int	is_alpha(char c)
{
	return ((c >= 'a' && c <= 'z')
		|| (c >= 'A' && c <= 'Z'));
}

int	ft_str_is_alpha(char *str)
{
	while (*str)
	{
		if (!is_alpha(*str++))
			return (0);
	}
	return (1);
}
