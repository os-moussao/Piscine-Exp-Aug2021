char	*ft_strlowcase(char *str)
{
	char	*root;

	if (!(str))
		return (str);
	root = str;
	while (*str)
	{
		if (*str >= 'A' && *str <= 'Z')
			*str += 'a' - 'A';
		str++;
	}
	return (root);
}
